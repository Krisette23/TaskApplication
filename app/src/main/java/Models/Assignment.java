package Models;

public class Assignment {
    public final int TaskId;
    public final int UserId;

    public Assignment (int TaskId, int UserId){
        this.TaskId = Taskid;
        this.UserId = Userid;
    }

    public int getTaskId() {
        return TaskId;
    }

    public int getUserId() {
        return UserId;
    }
}
